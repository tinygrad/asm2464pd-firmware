#!/usr/bin/env python3
"""
Careful probe of C480-C4FF: read everything first, then test writability
of specific safe-looking registers one at a time.
"""
import sys
sys.path.insert(0, "/home/geohot/tinygrad")
from tinygrad.runtime.support.usb import ASM24Controller

ctrl = ASM24Controller()

def r(addr): return ctrl.read(addr, 1)[0]
def rn(addr, n): return ctrl.read(addr, n)
def w(addr, val): ctrl.write(addr, bytes([val]), ignore_cache=True)

# Step 1: Read everything
print("="*60)
print("C480-C4FF full read")
print("="*60)
for base in range(0xC480, 0xC500, 16):
  d = rn(base, 16)
  nz = ' '.join(f'{b:02X}' for b in d)
  markers = ''.join('*' if b else '.' for b in d)
  print(f"  0x{base:04X}: {nz}  {markers}")

# Step 2: Test writability of only the zero-valued registers
# (safer — less likely to break things)
print("\n" + "="*60)
print("Writability test (zero-valued only, skip C48A/C48B)")
print("="*60)

# From the read, non-zero regs are:
# C487=02, C48A=20, C4B0-B5, C4CE=02, C4EB=01, C4EC=01, C4EF=01, C4F7=02
# Test only the ZERO regs (writing 0xAA, checking, restoring 0x00)

safe_zeros = [
  0xC480, 0xC481, 0xC482, 0xC483, 0xC484, 0xC485, 0xC486,
  # skip C487 (non-zero), C488, C489
  0xC48C, 0xC48D, 0xC48E, 0xC48F,
  0xC490, 0xC491, 0xC492, 0xC493, 0xC494, 0xC495, 0xC496, 0xC497,
  0xC498, 0xC499, 0xC49A, 0xC49B, 0xC49C, 0xC49D, 0xC49E, 0xC49F,
  0xC4A0, 0xC4A1, 0xC4A2, 0xC4A3, 0xC4A4, 0xC4A5, 0xC4A6, 0xC4A7,
  0xC4A8, 0xC4A9, 0xC4AA, 0xC4AB, 0xC4AC, 0xC4AD, 0xC4AE, 0xC4AF,
  # skip C4B0-B5 (non-zero, R/O)
  0xC4B6, 0xC4B7, 0xC4B8, 0xC4B9, 0xC4BA, 0xC4BB, 0xC4BC, 0xC4BD, 0xC4BE, 0xC4BF,
  0xC4C0, 0xC4C1, 0xC4C2, 0xC4C3, 0xC4C4, 0xC4C5, 0xC4C6, 0xC4C7,
  0xC4C8, 0xC4C9, 0xC4CA, 0xC4CB, 0xC4CC, 0xC4CD,
  # C4CE=0x02 skip
  0xC4CF,
  0xC4D0, 0xC4D1, 0xC4D2, 0xC4D3, 0xC4D4, 0xC4D5, 0xC4D6, 0xC4D7,
  0xC4D8, 0xC4D9, 0xC4DA, 0xC4DB, 0xC4DC, 0xC4DD, 0xC4DE, 0xC4DF,
  0xC4E0, 0xC4E1, 0xC4E2, 0xC4E3, 0xC4E4, 0xC4E5, 0xC4E6, 0xC4E7,
  0xC4E8, 0xC4E9, 0xC4EA,
  # C4EB-EF non-zero
  0xC4ED, 0xC4EE,  # try these
  0xC4F0, 0xC4F1, 0xC4F2, 0xC4F3, 0xC4F4, 0xC4F5, 0xC4F6,
  # C4F7=02
  0xC4F8, 0xC4F9, 0xC4FA, 0xC4FB,
  0xC4FC, 0xC4FD, 0xC4FE, 0xC4FF,
]

for addr in safe_zeros:
  try:
    orig = r(addr)
    w(addr, 0xAA)
    rb = r(addr)
    w(addr, orig)
    _ = r(0xC471)  # health check
    if rb == 0xAA:
      print(f"  0x{addr:04X}: R/W")
    elif rb != 0 and rb != orig:
      print(f"  0x{addr:04X}: orig=0x{orig:02X} wrote=0xAA read=0x{rb:02X} -> PARTIAL")
  except Exception as e:
    print(f"  0x{addr:04X}: CRASHED ({e})")
    break

# Step 3: CE scan
print("\n" + "="*60)
print("CE00-CEFF full read")
print("="*60)
for base in range(0xCE00, 0xCF00, 16):
  d = rn(base, 16)
  nz = ' '.join(f'{b:02X}' for b in d)
  markers = ''.join('*' if b else '.' for b in d)
  print(f"  0x{base:04X}: {nz}  {markers}")

# Step 4: CE writability (safe zeros only)
print("\n" + "="*60)
print("CE writability (safe zeros only)")
print("="*60)

ce_safe = [
  # CE01 is R/W from earlier but crashed - skip
  0xCE03, 0xCE04, 0xCE06, 0xCE07, 0xCE08, 0xCE09, 0xCE0A, 0xCE0B,
  0xCE0C, 0xCE0D, 0xCE0E, 0xCE0F,
  0xCE10, 0xCE11, 0xCE12, 0xCE13,
  0xCE14, 0xCE15, 0xCE16, 0xCE17, 0xCE18, 0xCE19, 0xCE1A, 0xCE1B,
  0xCE1C, 0xCE1D, 0xCE1E, 0xCE1F,
  0xCE23, 0xCE24, 0xCE25, 0xCE26, 0xCE27,
  0xCE28, 0xCE29, 0xCE2A, 0xCE2B, 0xCE2C, 0xCE2D, 0xCE2E, 0xCE2F,
  0xCE30, 0xCE31, 0xCE32, 0xCE33, 0xCE34, 0xCE35,
  0xCE37, 0xCE38, 0xCE39, 0xCE3B,
  0xCE3E,
  0xCE40, 0xCE41, 0xCE42, 0xCE43,
  0xCE4A, 0xCE4B, 0xCE4C, 0xCE4D, 0xCE4E, 0xCE4F,
  0xCE50, 0xCE52, 0xCE53, 0xCE54, 0xCE56, 0xCE57, 0xCE58, 0xCE59, 0xCE5A,
  0xCE5E,
  0xCE60, 0xCE61, 0xCE62,
  0xCE65, 0xCE68, 0xCE69, 0xCE6A, 0xCE6B,
  0xCE6E, 0xCE6F,
  0xCE70, 0xCE71, 0xCE72, 0xCE74, 0xCE75,
  0xCE76, 0xCE77, 0xCE78, 0xCE79,
  0xCE7A, 0xCE7B, 0xCE7C, 0xCE7D, 0xCE7E, 0xCE7F,
  0xCE84, 0xCE85,
  0xCE8E,
  0xCE90, 0xCE91, 0xCE92, 0xCE93,
  0xCE98, 0xCE99, 0xCE9A, 0xCE9B, 0xCE9C, 0xCE9D, 0xCE9E, 0xCE9F,
  0xCEA0, 0xCEA1, 0xCEA2, 0xCEA3, 0xCEA4, 0xCEA5, 0xCEA6, 0xCEA7,
  0xCEA8, 0xCEA9, 0xCEAA, 0xCEAB, 0xCEAC, 0xCEAD, 0xCEAE, 0xCEAF,
  0xCEB0, 0xCEB1, 0xCEB2, 0xCEB3,
]

for addr in ce_safe:
  try:
    orig = r(addr)
    tv = 0xAA if orig != 0xAA else 0x55
    w(addr, tv)
    rb = r(addr)
    w(addr, orig)
    _ = r(0xC471)  # health
    if rb == tv:
      print(f"  0x{addr:04X}: orig=0x{orig:02X} -> R/W")
    elif rb != orig:
      print(f"  0x{addr:04X}: orig=0x{orig:02X} wrote=0x{tv:02X} read=0x{rb:02X} -> PARTIAL")
  except Exception as e:
    print(f"  0x{addr:04X}: CRASHED ({e})")
    break

print("\nDone!")

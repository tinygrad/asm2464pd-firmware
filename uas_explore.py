    (0xC472, 0x00),  # REG_NVME_LINK_CTRL = 0x00
    (0xC448, 0xFF),  # REG_NVME_INIT_CTRL2 = 0xFF
    (0xC449, 0xFF),  # REG_NVME_INIT_CTRL2_1 = 0xFF
    (0xC44A, 0xFF),  # REG_NVME_INIT_CTRL2_2 = 0xFF

    # Phase 6: NVMe WRITE command setup (first)
    (0xC414, 0x82),  # REG_NVME_DATA_CTRL = 0x82
    (0xC401, 0x00),  # REG_NVME_STATUS = 0x00
    (0xC412, 0x03),  # REG_NVME_CTRL_STATUS = 0x03
    (0xC428, 0x30),  # REG_NVME_QUEUE_CFG = 0x30

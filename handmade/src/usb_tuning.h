/*
 * USB SS PHY tuning values from stock firmware.
 */

#include "types.h"
#include "registers.h"

static void rmw(uint16_t addr, uint8_t and_mask, uint8_t or_val) {
  XDATA_REG8(addr) = (XDATA_REG8(addr) & and_mask) | or_val;
}

static void usb_serdes_tune_lane(uint16_t base) {
  rmw(base + 0x02, 0x1F, 0xA0);
  rmw(base + 0x03, 0xF3, 0x00);
  rmw(base + 0x04, 0x8F, 0x40);
  rmw(base + 0x05, 0x0F, 0x60);
  rmw(base + 0x06, 0xF0, 0x07);
  rmw(base + 0x07, 0x1F, 0x60);
  rmw(base + 0x09, 0x0F, 0x90);
  rmw(base + 0x0B, 0xC0, 0x0A);
  rmw(base + 0x0C, 0xFD, 0x00);
  rmw(base + 0x10, 0xE0, 0x03);
  rmw(base + 0x11, 0xE0, 0x08);
  rmw(base + 0x12, 0x1F, 0x20);
  rmw(base + 0x13, 0xF3, 0x04);
  rmw(base + 0x14, 0xFF, 0x06);
  rmw(base + 0x15, 0xF0, 0x0C);
  rmw(base + 0x16, 0xF0, 0x0F);
  rmw(base + 0x17, 0x1F, 0x40);
  rmw(base + 0x19, 0x0F, 0x80);
  rmw(base + 0x1A, 0xF0, 0x0E);
  rmw(base + 0x1B, 0xC0, 0x00);
  rmw(base + 0x1C, 0xFD, 0x02);
  rmw(base + 0x20, 0xE0, 0x03);
  rmw(base + 0x21, 0xE0, 0x08);
  rmw(base + 0x22, 0xE0, 0x0A);
  rmw(base + 0x23, 0xFC, 0x02);
  rmw(base + 0x24, 0xF0, 0x07);
  rmw(base + 0x25, 0xF0, 0x0F);
  rmw(base + 0x26, 0xF0, 0x0B);
  rmw(base + 0x27, 0x1F, 0x40);
  rmw(base + 0x29, 0x0F, 0x80);
  rmw(base + 0x2A, 0xFF, 0x01);
  rmw(base + 0x2B, 0xC0, 0x00);
  rmw(base + 0x2C, 0xFD, 0x02);
  rmw(base + 0x3C, 0xFD, 0x00);
  rmw(base + 0x43, 0xC3, 0x1C);
  rmw(base + 0x45, 0xF0, 0x0B);
  rmw(base + 0x46, 0xF0, 0x0D);
  rmw(base + 0x49, 0x80, 0x41);
  rmw(base + 0x4A, 0xFE, 0x00);
  rmw(base + 0x4C, 0xF1, 0x0E);
  rmw(base + 0x4E, 0xFF, 0x40);
  rmw(base + 0x5B, 0xE0, 0x1B);
}

static void usb_apply_ss_phy_tuning(void) {
  usb_serdes_tune_lane(0xC280);  /* lane 0 */
  usb_serdes_tune_lane(0xC300);  /* lane 1 */
}

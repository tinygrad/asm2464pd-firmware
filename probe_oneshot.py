#!/usr/bin/env python3
"""
One-shot test: set ALL stock regs + CE76=0x200400, trigger, scan.
No cleanup. Just see if the target moved.
"""
import sys, ctypes
sys.path.insert(0, "/home/geohot/tinygrad")
from tinygrad.runtime.support.usb import USB3
from tinygrad.runtime.autogen import libusb

dev = USB3(0xADD1, 0x0001, 0x81, 0x83, 0x02, 0x04, use_bot=True)

def read8(addr):
  buf = (ctypes.c_ubyte * 1)()
  ret = libusb.libusb_control_transfer(dev.handle, 0xC0, 0xE4, addr, 0, buf, 1, 1000)
  assert ret >= 0
  return buf[0]

def write(addr, val):
  ret = libusb.libusb_control_transfer(dev.handle, 0x40, 0xE5, addr, val, None, 0, 1000)
  assert ret >= 0

# Send unique data
dev._bulk_out(0x02, bytes([0xEE]*32))

# Clear scan area
for off in range(0, 0x1000, 0x200):
  write(0xF000 + off, 0x00)

# ALL stock regs
regs = {
  0xC412: 0x03, 0xC413: 0x05, 0xC414: 0x80, 0xC415: 0x01,
  0xC421: 0x01, 0xC422: 0x02, 0xC427: 0x08, 0xC428: 0x30,
  0xC438: 0xFF, 0xC439: 0xFF, 0xC43A: 0xFF, 0xC43B: 0xFF,
  0xC448: 0xFF, 0xC449: 0xFF, 0xC44A: 0xFF, 0xC44B: 0xFF,
  0xC471: 0x01, 0xC473: 0x66, 0xC47B: 0x01,
  0xC4EB: 0x01, 0xC4EC: 0x01, 0xC4EF: 0x01, 0xC4F7: 0x01,
  0xC50C: 0x11, 0xC516: 0x01, 0xC517: 0x01,
  0xC518: 0x08, 0xC519: 0x08, 0xC51E: 0x01, 0xC51F: 0x01,
  0xC523: 0x01, 0xC530: 0xE0, 0xC531: 0xE1, 0xC533: 0x01,
  0xC534: 0x80, 0xC536: 0x0C, 0xC537: 0x08, 0xC539: 0x1F, 0xC542: 0x01,
  0xC800: 0x05, 0xC801: 0x50, 0xC802: 0x04, 0xC805: 0x02,
  0xC807: 0x84, 0xC809: 0x2A,
  0xC8A2: 0x80, 0xC8A4: 0x04, 0xC8A8: 0x01,
  0xC8AA: 0x03, 0xC8AB: 0x01, 0xC8AC: 0x07,
  0xC8B2: 0xBC, 0xC8B3: 0x80, 0xC8B4: 0xFF, 0xC8B5: 0xFF, 0xC8B6: 0x14,
  # TARGET ADDRESS: CE76-79 = PCI 0x00200400 = XDATA F400
  0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00,
}

for addr, val in sorted(regs.items()):
  write(addr, val)

# Trigger
write(0xC42A, 0x00)
write(0xC400, 1); write(0xC401, 1); write(0xC402, 1); write(0xC404, 1)
write(0xC406, 1)

# Scan
print("Scan F000-FFFF:")
for off in range(0, 0x1000, 0x200):
  d = read8(0xF000 + off)
  marker = " <-- HERE" if d == 0xEE else ""
  print(f"  F{off:03X}: 0x{d:02X}{marker}")

write(0xC42A, 0x01)
print("Done!")

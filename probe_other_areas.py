test("C405=4", {0xC405: 0x04})
test("C405=4 CE76=0x200400", {0xC405: 0x04, 0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00})
test("C509=4", {0xC509: 0x04})

# Test CCxx regs that differ
test("CC12=3 CC13=0xE7", {0xCC12: 0x03, 0xCC13: 0xE7})
test("CC2A=4", {0xCC2A: 0x04})
test("CC2C=0xC7 CC2D=0xC7", {0xCC2C: 0xC7, 0xCC2D: 0xC7})

# B2xx PCIe regs
test("B217=0x0F B218=1", {0xB217: 0x0F, 0xB218: 0x01})

print("\nDone!")

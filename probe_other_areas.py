#!/usr/bin/env python3
"""
Set ALL stock regs (C4+C5+C8+CC+CE+9xxx) then try address candidates.
Maybe the DMA address only works when the full system state matches stock.
"""
import sys, ctypes
sys.path.insert(0, "/home/geohot/tinygrad")
from tinygrad.runtime.support.usb import USB3
from tinygrad.runtime.autogen import libusb

dev = USB3(0xADD1, 0x0001, 0x81, 0x83, 0x02, 0x04, use_bot=True)

def read8(addr):
  buf = (ctypes.c_ubyte * 1)()
  ret = libusb.libusb_control_transfer(dev.handle, 0xC0, 0xE4, addr, 0, buf, 1, 1000)
  assert ret >= 0
  return buf[0]

def write(addr, val):
  ret = libusb.libusb_control_transfer(dev.handle, 0x40, 0xE5, addr, val, None, 0, 1000)
  assert ret >= 0

test_n = [0]

# ALL stock register values that differ from handmade (from the full diff)
# Excluding volatile/status regs, focusing on config regs
STOCK_ALL = {
  # C4xx
  0xC412: 0x03, 0xC413: 0x05, 0xC414: 0x80, 0xC415: 0x01,
  0xC421: 0x01, 0xC422: 0x02, 0xC427: 0x08, 0xC428: 0x30,
  0xC438: 0xFF, 0xC439: 0xFF, 0xC43A: 0xFF, 0xC43B: 0xFF,
  0xC448: 0xFF, 0xC449: 0xFF, 0xC44A: 0xFF, 0xC44B: 0xFF,
  0xC471: 0x01, 0xC473: 0x66, 0xC47B: 0x01,
  0xC4EB: 0x01, 0xC4EC: 0x01, 0xC4EF: 0x01, 0xC4F7: 0x01,
  # C5xx
  0xC50C: 0x11, 0xC516: 0x01, 0xC517: 0x01,
  0xC518: 0x08, 0xC519: 0x08, 0xC51E: 0x01, 0xC51F: 0x01,
  0xC523: 0x01, 0xC530: 0xE0, 0xC531: 0xE1, 0xC533: 0x01,
  0xC534: 0x80, 0xC536: 0x0C, 0xC537: 0x08, 0xC539: 0x1F, 0xC542: 0x01,
  # C8xx
  0xC800: 0x05, 0xC801: 0x50, 0xC802: 0x04, 0xC805: 0x02,
  0xC807: 0x84, 0xC809: 0x2A, 0xC89D: 0xFF, 0xC8A2: 0x80,
  0xC8A4: 0x04, 0xC8A8: 0x01, 0xC8AA: 0x03, 0xC8AB: 0x01,
  0xC8AC: 0x07, 0xC8B2: 0xBC, 0xC8B3: 0x80,
  0xC8B4: 0xFF, 0xC8B5: 0xFF, 0xC8B6: 0x14,
  # CExx
  0xCE86: 0x08, 0xCE87: 0x11, 0xCE89: 0x03,
  0xCE8A: 0x05, 0xCE8F: 0x01,
  0xCEB0: 0x01, 0xCEB3: 0x01,
  0xCEC0: 0xE4, 0xCEC1: 0x01, 0xCEC2: 0x50, 0xCEC3: 0xCE,
  0xCEC4: 0xC4, 0xCECD: 0x08,
}

def test(label, extra_writes):
  test_n[0] += 1
  marker = (test_n[0] * 11 + 0x37) & 0xFF
  if marker in (0, 0x55): marker = 0x77

  dev._bulk_out(0x02, bytes([marker]*32))
  for off in range(0, 0x1000, 0x200):
    write(0xF000 + off, 0x00)

  # Set ALL stock regs
  for addr, val in STOCK_ALL.items():
    write(addr, val)
  # Set extra
  for addr, val in extra_writes.items():
    write(addr, val)

  # Trigger
  write(0xC42A, 0x00)
  write(0xC400, 1); write(0xC401, 1); write(0xC402, 1); write(0xC404, 1)
  write(0xC406, 1)

  landed = []
  for off in range(0, 0x1000, 0x200):
    d = read8(0xF000 + off)
    if d == marker:
      landed.append(f"F{off:03X}")

  write(0xC42A, 0x01)
  for addr in STOCK_ALL: write(addr, 0x00)
  for addr in extra_writes: write(addr, 0x00)

  moved = landed and landed[0] != 'F000'
  locs = ', '.join(landed) if landed else 'nowhere'
  flag = " ** MOVED **" if moved else ""
  print(f"  {label:50s}: {locs}{flag}")

# ============================================================
print("="*60)
print("Full stock state + address candidates")
print("="*60)

test("baseline (all stock)", {})

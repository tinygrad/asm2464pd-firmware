test("C416-19 = LE 0x200400", {0xC416: 0x00, 0xC417: 0x04, 0xC418: 0x20, 0xC419: 0x00})
test("C4EE=4 C4EF=0", {0xC4EE: 0x04, 0xC4EF: 0x00})

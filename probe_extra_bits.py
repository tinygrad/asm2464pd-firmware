#!/usr/bin/env python3
"""
Set extra bits on top of known-good base. Fresh marker per test.
Properly clear all non-base regs to 0 before each test.
"""
import sys, ctypes
sys.path.insert(0, "/home/geohot/tinygrad")
from tinygrad.runtime.support.usb import USB3
from tinygrad.runtime.autogen import libusb

dev = USB3(0xADD1, 0x0001, 0x81, 0x83, 0x02, 0x04, use_bot=True)
test_counter = [0]

def read8(addr):
  buf = (ctypes.c_ubyte * 1)()
  ret = libusb.libusb_control_transfer(dev.handle, 0xC0, 0xE4, addr, 0, buf, 1, 1000)
  assert ret >= 0
  return buf[0]

def write(addr, val):
  ret = libusb.libusb_control_transfer(dev.handle, 0x40, 0xE5, addr, val, None, 0, 1000)
  assert ret >= 0

BASE_DESC = [
  (0xC420, 0x00), (0xC421, 0x01), (0xC422, 0x02), (0xC423, 0x00),
  (0xC424, 0x00), (0xC425, 0x00), (0xC426, 0x00), (0xC427, 0x01),
  (0xC428, 0x30), (0xC429, 0x00), (0xC42A, 0x00), (0xC42B, 0x00),
  (0xC42C, 0x00), (0xC42D, 0x00),
]
BASE_ARM = [(0xC400, 1), (0xC401, 1), (0xC402, 1), (0xC404, 1)]

# All writable non-base regs to clear
CLEAR_REGS = [0xC403, 0xC405, 0xC408, 0xC409, 0xC40C, 0xC40D,
              0xC412, 0xC413, 0xC414, 0xC415, 0xC416, 0xC417, 0xC418, 0xC419]

def test(label, extra_writes):
  test_counter[0] += 1
  marker = (test_counter[0] * 13 + 0x23) & 0xFF
  if marker in (0x00, 0x55): marker = 0x77
  
  dev._bulk_out(0x02, bytes([marker]*32))
  
  for off in range(0, 0x1000, 0x100):
    write(0xF000 + off, 0x00)
  
  # Clear all non-base writable regs
  for reg in CLEAR_REGS:
    write(reg, 0x00)
  
  # Set extras BEFORE descriptor/arm
  for addr, val in extra_writes:
    write(addr, val)
  
  # Apply descriptor
  for addr, val in BASE_DESC:
    write(addr, val)
  
  # Arm
  for addr, val in BASE_ARM:
    write(addr, val)
  
  # Also apply extras that are arm regs (override)
  for addr, val in extra_writes:
    if 0xC400 <= addr <= 0xC405:
      write(addr, val)
  
  # Trigger
  write(0xC406, 0x01)
  
  landed = []
  for off in range(0, 0x1000, 0x100):
    d = read8(0xF000 + off)
    if d == marker:
      landed.append(off)
  
  write(0xC42A, 0x01)
  
  locs = ', '.join(f'F{off:03X}' for off in landed)
  fired = len(landed) > 0
  moved = fired and landed[0] != 0
  flag = " ** MOVED **" if moved else (" NO_DMA" if not fired else "")
  if len(landed) > 1: flag += f" ({len(landed)} hits)"
  print(f"  {label:45s}: {locs if locs else 'nowhere'}{flag}")
  return moved

# Verify
print("Control: ", end="")
test("base only", [])

print("\n" + "="*60)
print("Extra bits on top of base (fresh marker per test)")
print("="*60)

for reg, vals in [
  (0xC403, [0x01, 0x02, 0x03, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC405, [0x01, 0x02, 0x03, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC408, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC409, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC40C, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC40D, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC412, [0x01, 0x02, 0x03]),
  (0xC413, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20]),
  (0xC414, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20]),
  (0xC415, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20]),
  (0xC416, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC417, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC418, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
  (0xC419, [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40]),
]:
  for val in vals:
    try:
      moved = test(f"0x{reg:04X}=0x{val:02X}", [(reg, val)])
    except:
      print(f"  0x{reg:04X}=0x{val:02X}: CRASH")
      break

print("\nDone!")

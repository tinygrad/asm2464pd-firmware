#!/usr/bin/env python3
"""
Set ALL stock C5xx values, THEN try address registers.
Maybe the C5xx state enables an addressing mode.
"""
import sys, ctypes
sys.path.insert(0, "/home/geohot/tinygrad")
from tinygrad.runtime.support.usb import USB3
from tinygrad.runtime.autogen import libusb

dev = USB3(0xADD1, 0x0001, 0x81, 0x83, 0x02, 0x04, use_bot=True)

def read8(addr):
  buf = (ctypes.c_ubyte * 1)()
  ret = libusb.libusb_control_transfer(dev.handle, 0xC0, 0xE4, addr, 0, buf, 1, 1000)
  assert ret >= 0
  return buf[0]

def write(addr, val):
  ret = libusb.libusb_control_transfer(dev.handle, 0x40, 0xE5, addr, val, None, 0, 1000)
  assert ret >= 0

test_n = [0]

ALL_STOCK_C4 = {
  0xC412: 0x03, 0xC413: 0x05, 0xC414: 0x80, 0xC415: 0x01,
  0xC421: 0x01, 0xC422: 0x02, 0xC427: 0x08, 0xC428: 0x30,
  0xC438: 0xFF, 0xC439: 0xFF, 0xC43A: 0xFF, 0xC43B: 0xFF,
  0xC448: 0xFF, 0xC449: 0xFF, 0xC44A: 0xFF, 0xC44B: 0xFF,
  0xC471: 0x01, 0xC473: 0x66, 0xC47B: 0x01,
  0xC4EB: 0x01, 0xC4EC: 0x01, 0xC4ED: 0x01, 0xC4EF: 0x01,
  0xC4F7: 0x01,
}
ALL_STOCK_C5 = {
  0xC50C: 0x11, 0xC516: 0x01, 0xC517: 0x01,
  0xC518: 0x08, 0xC519: 0x08, 0xC51E: 0x01, 0xC51F: 0x01,
  0xC523: 0x01, 0xC530: 0xE0, 0xC531: 0xE1, 0xC533: 0x01,
  0xC534: 0x80, 0xC536: 0x0C, 0xC537: 0x08, 0xC539: 0x1F, 0xC542: 0x01,
}
ALL_STOCK = {**ALL_STOCK_C4, **ALL_STOCK_C5}

def test(label, extra_writes):
  test_n[0] += 1
  marker = (test_n[0] * 11 + 0x37) & 0xFF
  if marker in (0, 0x55): marker = 0x77

  dev._bulk_out(0x02, bytes([marker]*32))
  for off in range(0, 0x1000, 0x200):
    write(0xF000 + off, 0x00)

  # Set ALL stock C4xx + C5xx
  for addr, val in ALL_STOCK.items():
    write(addr, val)

  # Set extra writes (address candidates)
  for addr, val in extra_writes.items():
    write(addr, val)

  # Full trigger
  write(0xC420, 0x00); write(0xC421, 0x01); write(0xC422, 0x02)
  write(0xC423, 0x00); write(0xC424, 0x00); write(0xC425, 0x00)
  write(0xC426, 0x00); write(0xC427, 0x01); write(0xC428, 0x30)
  write(0xC429, 0x00); write(0xC42A, 0x00); write(0xC42B, 0x00)
  write(0xC42C, 0x00); write(0xC42D, 0x00)
  write(0xC400, 1); write(0xC401, 1); write(0xC402, 1); write(0xC404, 1)
  write(0xC406, 1)

  landed = []
  for off in range(0, 0x1000, 0x200):
    d = read8(0xF000 + off)
    if d == marker:
      landed.append(f"F{off:03X}")

  write(0xC42A, 0x01)
  # Clean up
  for addr in ALL_STOCK: write(addr, 0x00)
  for addr in extra_writes: write(addr, 0x00)

  moved = landed and landed[0] != 'F000'
  flag = " ** MOVED **" if moved else ""
  locs = ', '.join(landed) if landed else 'nowhere'
  print(f"  {label:50s}: {locs}{flag}")
  return moved

# ============================================================
print("="*60)
print("All stock C5xx + address candidates")
print("="*60)

test("baseline (C5xx only)", {})

# CE76-79 with C5xx
test("CE76-79 = PCI 0x200400", {0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00})

# C416-C419 with C5xx
test("C416-19 = LE PCI 0x200400", {0xC416: 0x00, 0xC417: 0x04, 0xC418: 0x20, 0xC419: 0x00})

# C4EE/C4EF with C5xx
test("C4EE=0x04 C4EF=0x00", {0xC4EE: 0x04, 0xC4EF: 0x00})
test("C4EE=0x00 C4EF=0x04", {0xC4EE: 0x00, 0xC4EF: 0x04})

# C423-C426 as offset with C5xx
test("C423=0x04", {0xC423: 0x04})
test("C424=0x02", {0xC424: 0x02})
test("C425=0x04", {0xC425: 0x04})

# C403 + C405 as combined addr/size with C5xx
test("C403=4 C405=4", {0xC403: 0x04, 0xC405: 0x04})

# C509 with C5xx
test("C509=0x04", {0xC509: 0x04})
test("C509=0x08", {0xC509: 0x08})

# C4ED as addr enable with C5xx + CE76
test("C4ED=1 + CE76=0x200400", {0xC4ED: 0x01, 0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00})

# CECD with C5xx (changed between 1KB/4KB: 0x02->0x08)
test("CECD=0x08", {0xCECD: 0x08})

# Full combo: C5xx + C4xx stock + CExx stock
STOCK_C4 = {0xC412: 0x03, 0xC413: 0x01, 0xC414: 0x80, 0xC415: 0x01, 0xC471: 0x01, 0xC473: 0x66}
test("C5xx + C4xx stock", STOCK_C4)
test("C5xx + C4xx + CE76=0x200400", {**STOCK_C4, 0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00})

# All stock + C403 for bigger DMA + CE76 for address
test("C5xx + C4xx + C403=4 + CE76=0x200400", 
     {**STOCK_C4, 0xC403: 0x04, 0xCE76: 0x00, 0xCE77: 0x04, 0xCE78: 0x20, 0xCE79: 0x00})

print("\nDone!")
